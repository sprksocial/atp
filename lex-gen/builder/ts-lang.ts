export function isReservedWord(word: string): boolean {
  return RESERVED_WORDS.has(word);
}

export function isSafeIdentifier(name: string): boolean {
  return !isReservedWord(name) && /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(name);
}
